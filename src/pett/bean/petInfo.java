package pett.bean;

public class petInfo {
    private String petId;
    private String petKind;
    private String petSex;
    private String petAge;
    private String petAddress;
    private String mrPetName;
    private String mrPetTel;

    public String getPetId() {
        return petId;
    }

    public void setPetId(String petId) {
        this.petId = petId;
    }

    public String getPetKind() {
        return petKind;
    }

    public void setPetKind(String petKind) {
        this.petKind = petKind;
    }

    public String getPetSex() {
        return petSex;
    }

    public void setPetSex(String petSex) {
        this.petSex = petSex;
    }

    public String getPetAge() {
        return petAge;
    }

    public void setPetAge(String petAge) {
        this.petAge = petAge;
    }

    public String getPetAddress() {
        return petAddress;
    }

    public void setPetAddress(String petAddress) {
        this.petAddress = petAddress;
    }

    public String getMrPetName() {
        return mrPetName;
    }

    public void setMrPetName(String mrPetName) {
        this.mrPetName = mrPetName;
    }

    public String getMrPetTel() {
        return mrPetTel;
    }

    public void setMrPetTel(String mrPetTel) {
        this.mrPetTel = mrPetTel;
    }
}
